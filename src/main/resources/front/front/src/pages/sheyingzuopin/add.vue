          zuopinfenlei: '',
          fengmian: '',
          paisheshijian: '',
          paishedidian: '',
          faburiqi: '',
          zuopinneirong: '',
          onelimittimes: Number('-1') ,
          alllimittimes: Number('-1') ,
          toupiaopiaoshu: Number('0') ,
          thumbsupnum: '',
          crazilynum: '',
          clicktime: '',
          clicknum: '',
          discussnum: '',
          price: '',
          vipprice: '',
          storeupnum: '',
        },
        zuopinfenleiOptions: [],


        rules: {
          zuopinmingcheng: [
            { required: true, message: '作品名称不能为空', trigger: 'blur' },
