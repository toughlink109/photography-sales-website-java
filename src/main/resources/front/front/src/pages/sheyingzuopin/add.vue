            { validator: this.$validate.isIntNumer, trigger: 'blur' },
          ],
          toupiaopiaoshu: [
            { required: true, message: '投票票数不能为空', trigger: 'blur' },
            { validator: this.$validate.isIntNumer, trigger: 'blur' },
