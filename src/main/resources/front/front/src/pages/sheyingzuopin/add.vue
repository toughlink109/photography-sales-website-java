				crazilynum : false,
				clicktime : false,
				clicknum : false,
				discussnum : false,
				price : false,
				vipprice : false,
				storeupnum : false,
        },
        type: '',
        userTableName: localStorage.getItem('UserTableName'),
        ruleForm: {
