<script>
// 数字，邮件，手机，url，身份证校验
import { isNumber,isIntNumer,isEmail,isMobile,isPhone,isURL,checkIdCard } from "@/utils/validate";

export default {
  data() {
    return {
      ruleForm: {},
      flag: '',
      adminsFlag: false,
      ordinaryUseruserSexOptions: [],
      ordinaryUservipOptions: [],
    };
  },
  mounted() {
    var table = this.$storage.get("sessionTable");
    this.flag = table;
    this.$http({
      url: `${this.$storage.get("sessionTable")}/session`,
      method: "get"
    }).then(({ data }) => {
      if (data && data.code === 0) {
        this.ruleForm = data.data;
      } else {
        this.$message.error(data.msg);
      }
    });
    this.ordinaryUseruserSexOptions = "男,女".split(',')
    this.ordinaryUservipOptions = "是,否".split(',')
  },
  methods: {
    ordinaryUseruserAvatarUploadChange(fileUrls) {
        this.ruleForm.userAvatar = fileUrls;
    },
	adminsimageUploadChange(fileUrls) {
		this.ruleForm.image = fileUrls;
	},
    onUpdateHandler() {
      if((!this.ruleForm.account)&& 'ordinaryUser'==this.flag){
        this.$message.error('账号不能为空');
        return
      }


      if((!this.ruleForm.password)&& 'ordinaryUser'==this.flag){
        this.$message.error('密码不能为空');
        return
      }








      if( 'ordinaryUser' ==this.flag && this.ruleForm.mobile&&(!isMobile(this.ruleForm.mobile))){
        this.$message.error(`手机应输入手机格式`);
        return
      }






        if(this.ruleForm.userAvatar!=null) {
                this.ruleForm.userAvatar = this.ruleForm.userAvatar.replace(new RegExp(this.$base.url,"g"),"");
        }


      if( 'ordinaryUser' ==this.flag && this.ruleForm.money&&(!isNumber(this.ruleForm.money))){
        this.$message.error(`余额应输入数字`);
        return
      }


      if('admins'==this.flag && this.ruleForm.username.trim().length<1) {
	this.$message.error(`用户名不能为空`);
        return	
      }
	  if(this.flag=='admins'){
	  	this.ruleForm.image = this.ruleForm.image.replace(new RegExp(this.$base.url,"g"),"")
	  }
      this.$http({
        url: `${this.$storage.get("sessionTable")}/update`,
        method: "post",
        data: this.ruleForm
      }).then(({ data }) => {
        if (data && data.code === 0) {
          this.$message({
            message: "修改信息成功",
            type: "success",
            duration: 1500,
            onClose: () => {
				if(this.flag=='admins'){
					this.$storage.set('headImage',this.ruleForm.image)
				}
            }
          });
        } else {
          this.$message.error(data.msg);
        }
      });
    }
  }
};
</script>
