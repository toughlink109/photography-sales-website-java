				var obj = this.$storage.getObj('crossObj');
				for (var o in obj){
						if(o=='title'){
							this.ruleForm.title = obj[o];
							this.ro.title = true;
							continue;
						}
						if(o=='workCategory'){
							this.ruleForm.workCategory = obj[o];
							this.ro.workCategory = true;
							continue;
